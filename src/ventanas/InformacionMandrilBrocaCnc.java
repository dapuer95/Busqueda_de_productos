        try {
            //Rellena la información del inserto

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/catalogo", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from arbol_mandril_cnc where (codigo = ?) ");

            pst.setString(1, cod.trim());

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {

                txt_codigo.setText(rs.getString("codigo"));
                txt_descripcion.setText(rs.getString("descripcion"));
                txt_acople.setText(rs.getString("acople"));
                txt_j.setText(rs.getString("j"));
                txt_l.setText(rs.getString("l"));
                txt_l1.setText(rs.getString("l1"));
                txt_l2.setText(rs.getString("l2"));
                txt_d1.setText(rs.getString("d1"));
                txt_a.setText(rs.getString("a"));

                codigo = rs.getString("codigo");
                form = rs.getString("j");

                ImageIcon imagen1 = new ImageIcon("src/images/mandril_broca_cnc_dim" + form + ".png");
                Icon icono1 = new ImageIcon(imagen1.getImage());
                label_dim.setIcon(icono1);

                if (form.equals("MT3")) {
                    label_d1.setText("d2(mm)");
                }
            }
        } catch (Exception e) {
        }
