    public InformacionMicrometros() {
        initComponents();
        setTitle("Información del micrometro");
        this.setLocationRelativeTo(null);
        this.setResizable(false);
        this.getContentPane().setBackground(Color.WHITE);
        cod = Micrometros.codigo2;
        this.getContentPane().setBackground(Color.WHITE);

        try {
            //Rellena la información del inserto
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/catalogo", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from micrometro where (codigo = ?) ");

            pst.setString(1, cod.trim());

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {

                txt_codigo.setText(rs.getString("codigo"));
                txt_descripcion.setText(rs.getString("descripcion"));
                txt_rango.setText(rs.getString("rango"));
                txt_resolucionmm.setText(rs.getString("resolucionmm"));
                txt_presicion.setText(rs.getString("precision"));
                //txt_piezas.setText(rs.getString("mordaza"));           
                txt_tipo.setText(rs.getString("tipo"));
                txt_especificacion.setText(rs.getString("especificacion"));
                txt_piezas.setText(rs.getString("piezas"));

                String tipo = rs.getString("tipo");
                String espe = rs.getString("especificacion");
                codigo = rs.getString("codigo");

                ImageIcon imagen1 = new ImageIcon("src/images/" + tipo + "_" + espe + "_" + "foto" + ".png");
                Icon icono1 = new ImageIcon(imagen1.getImage());
                label_foto.setIcon(icono1);

                ImageIcon imagen2 = new ImageIcon("src/images/" + tipo + "_" + espe + "_" + "dim" + ".png");
                Icon icono2 = new ImageIcon(imagen2.getImage());
                label_dim.setIcon(icono2);
            }
        } catch (Exception e) {
        }
    }

